###############################################################################
# Load Framework
###############################################################################

source("../config.R")

source("../benchmark/benchmark_interface.R")
source("../benchmark/benchmark_utils.R")

source("../benchmark/g10.R")

###############################################################################
# Header
###############################################################################

cat("\n")
cat("=============================================\n")
cat("G10 KNOWN SOLUTION TEST\n")
cat("=============================================\n\n")

###############################################################################
# Known Optimum (CEC2006)
###############################################################################

x <- c(
  
  579.3066850179796,
  1359.9706780793560,
  5109.9706574313332,
  182.0176996306153,
  295.6011737027468,
  217.9823003693846,
  286.4165259278685,
  395.6011737027467
  
)

###############################################################################
# Evaluation
###############################################################################

result <- G10$evaluate(x)

cat("Objective ........:", result$objective, "\n\n")

cat("Constraints\n")

for(i in seq_along(result$constraints)){
  
  cat(
    
    sprintf(
      
      "[%d] % .12e\n",
      
      i,
      
      result$constraints[i]
      
    )
    
  )
  
}

cat("\n")

cat("Violation ........:", result$violation, "\n")

cat("Feasible .........:", result$feasible, "\n")

cat("\n")

cat("Known optimum ....:", G10$knownOptimum, "\n")

cat("\n")

###############################################################################
# Assertions
###############################################################################

stopifnot(
  
  abs(
    
    result$objective -
      G10$knownOptimum
    
  ) < 1e-3
  
)

stopifnot(
  
  result$violation <=
    CONSTRAINT_TOLERANCE
  
)

stopifnot(
  
  result$feasible
  
)

###############################################################################
# Success
###############################################################################

cat("\n")
cat("=============================================\n")
cat("KNOWN SOLUTION ACCEPTED\n")
cat("=============================================\n")