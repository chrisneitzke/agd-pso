#       f(x*) = 7049.2480205286
#
#    Dimension:
#       8
#
#    Constraints:
#       6 nonlinear inequalities
#
###############################################################################

G10 <- createBenchmark(
  
  name = "G10",
  
  dimension = 8,
  
  lowerBound = c(
    100,
    1000,
    1000,
    10,
    10,
    10,
    10,
    10
  ),
  
  upperBound = c(
    10000,
    10000,
    10000,
    1000,
    1000,
    1000,
    1000,
    1000
  ),
  
  knownOptimum = 7049.2480205286,
  
  evaluateFunction = function(x){
    
    ###########################################################################
    # Objective function
    ###########################################################################
    
    objective <-
      x[1] +
      x[2] +
      x[3]
    
    ###########################################################################
    # Constraints (g(x) <= 0)
    ###########################################################################
    
    constraints <- c(
      
      -1 +
        0.0025 * (x[4] + x[6]),
      
      -1 +
        0.0025 * (-x[4] + x[5] + x[7]),
      
      -1 +
        0.01 * (-x[5] + x[8]),
      
      100 * x[1] -
        x[1] * x[6] +
        833.33252 * x[4] -
        83333.333,
      
      x[2] * x[4] -
        x[2] * x[7] -
        1250 * x[4] +
        1250 * x[5],
      
      x[3] * x[5] -
        x[3] * x[8] -
        2500 * x[5] +
        1250000
      
    )
    
    ###########################################################################
    # Constraint handling
    ###########################################################################
    
    violation <-
      computeViolation(
        constraints
      )
    
    feasible <-
      isFeasible(
        violation
      )
    
    ###########################################################################
    # Return
    ###########################################################################
    
    list(
      
      objective = objective,
      
      constraints = constraints,
      
      violation = violation,
      
      feasible = feasible
      
    )
    
  }
  
)