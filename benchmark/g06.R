G06 <- createBenchmark(
  
  name = "G06",
  
  dimension = 2,
  
  lowerBound = c(13, 0),
  
  upperBound = c(100, 100),
  
  knownOptimum = -6961.813875,
  
  evaluateFunction = function(x){
    
    ###########################################################################
    # Objective function
    ###########################################################################
    
    objective <-
      (x[1] - 10)^3 +
      (x[2] - 20)^3
    
    ###########################################################################
    # Constraints (g(x) <= 0)
    ###########################################################################
    
    constraints <- c(
      
      -(x[1] - 5)^2 -
        (x[2] - 5)^2 +
        100,
      
      (x[1] - 6)^2 +
        (x[2] - 5)^2 -
        82.81
      
    )
    
    ###########################################################################
    # Constraint handling
    ###########################################################################
    
    violation <-
      computeViolation(
        constraints
      )
    
    feasible <-
      isFeasible(
        violation
      )
    
    ###########################################################################
    # Return
    ###########################################################################
    
    list(
      
      objective = objective,
      
      constraints = constraints,
      
      violation = violation,
      
      feasible = feasible
      
    )
    
  }
  
)