#    g01.R
#
# Description:
#    CEC 2006 - G01 Benchmark
#
#    Optimal value:
#       f(x*) = -15.000000
#
#    Dimension:
#       13
#
#    Constraints:
#       9 linear inequalities
#
###############################################################################

G01 <- createBenchmark(
  
  name = "G01",
  
  dimension = 13,
  
  lowerBound = c(
    rep(0, 9),
    rep(0, 3),
    0
  ),
  
  upperBound = c(
    rep(1, 9),
    rep(100, 3),
    1
  ),
  
  knownOptimum = -15,
  
  evaluateFunction = function(x){
    
    ###########################################################################
    # Objective Function
    ###########################################################################
    
    objective <-
      5 * sum(x[1:4]) -
      5 * sum(x[1:4]^2) -
      sum(x[5:13])
    
    ###########################################################################
    # Constraints (g(x) <= 0)
    ###########################################################################
    
    constraints <- c(
      
      2*x[1] + 2*x[2] + x[10] + x[11] - 10,
      
      2*x[1] + 2*x[3] + x[10] + x[12] - 10,
      
      2*x[2] + 2*x[3] + x[11] + x[12] - 10,
      
      -8*x[1] + x[10],
      
      -8*x[2] + x[11],
      
      -8*x[3] + x[12],
      
      -2*x[4] - x[5] + x[10],
      
      -2*x[6] - x[7] + x[11],
      
      -2*x[8] - x[9] + x[12]
      
    )
    
    ###########################################################################
    # Constraint Handling
    ###########################################################################
    
    violation <-
      computeViolation(
        constraints
      )
    
    feasible <-
      isFeasible(
        violation
      )
    
    ###########################################################################
    # Return
    ###########################################################################
    
    return(
      
      list(
        
        objective = objective,
        
        constraints = constraints,
        
        violation = violation,
        
        feasible = feasible
        
      )
      
    )
    
  }
  
)