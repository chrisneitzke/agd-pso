###############################################################################
# Creates a new individual.
###############################################################################

createIndividual <- function(dimension){
  
  individual <- list(
    
    #######################################################################
    # Identification
    #######################################################################
    
    id = NA_integer_,
    
    #######################################################################
    # Decision variables
    #######################################################################
    
    x = numeric(dimension),
    
    #######################################################################
    # Objective function
    #######################################################################
    
    objective = Inf,
    
    #######################################################################
    # Constraint values
    #######################################################################
    
    constraints = NULL,
    
    #######################################################################
    # Total constraint violation
    #######################################################################
    
    violation = Inf,
    
    ###########################################################################
    # Feasibility
    ###########################################################################
    
    feasible = FALSE,
    
    #######################################################################
    # Penalized fitness
    #######################################################################
    
    fitness = Inf,
    
    #######################################################################
    # PSO information
    #######################################################################
    
    velocity = numeric(dimension),
    
    pbest = numeric(dimension),
    
    pbestFitness = Inf,
    
    #######################################################################
    # GSO information
    #######################################################################
    
    neighbors = 0
    
  )
  
  return(individual)
  
}

###############################################################################
# Creates a copy of an individual.
###############################################################################

cloneIndividual <- function(individual){
  
  clone <- individual
  
  return(clone)
  
}